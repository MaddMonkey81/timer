    <h3>CrossFit Workout Terminology: WODs, EMOMs and AMRAPs</h3>
    
    <p>With more AMRAPs, EMOMs and WODs than you can get your head around, it makes sense to come to term's with CrossFit's terminology before attempting any of the below.</p>
    
    <h3>AMRAP:</h3> 
    
    <p>This stands for "as many reps (or rounds) as possible", a workout structure that's frequently used in CrossFit to accompany conditioning pieces. It's designed to push your body to the max in a short time frame. "AMRAPs allow you to specify the amount of time that a training session will take. If you know that you'll be going for exactly 7 minutes, it's much easier to understand the pacing of the effort required over that 7 minutes,”said Todd Nief, head CrossFit coach and owner of South Loop Strength & Conditioning in Chicago. Related Story Neturalise Fat With this CrossFit Workout</p>
    
    <h3>EMOM:</h3> 
    
    <p>Every minute, on the minute. You'll need a watch, stopwatch or a clock for this one. Complete the prescribed number of reps within a minute's timeframe, resting for the remainder of the same minute. The EMOM format encourages you to work harder in order to earn more rest. Once a new minute starts, so do you. Deep breaths, now.</p>
    
    <h3>WOD:</h3> 
    
    <p>Workout of the day — the prescribed workout from CrossFit Affiliate gyms to their members. Usually under 20 minutes, some people question their efficacy. "Each person will need to experiment to determine what "enough" means," says the official CrossFit website. "Experienced athletes with specific competition goals might need additional work to improve their fitness, while beginners might need to reduce the volume of the WOD to optimise results."</p>
    
    <h3>RFT:</h3> 
    
    <p>Rounds for time — this means that you have to go hell-for-leather to achieve the quickest time possible while maintaining good form. RFT workouts are particularly useful for measuring yourself against previous times and thereby testing your strength and your fitness.</p> 

